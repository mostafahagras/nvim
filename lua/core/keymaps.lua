-- Set leader key
vim.g.mapleader = " "
vim.g.maplocalleader = " "

-- Disable the spacebar key's default behavior in Normal and Visual modes
vim.keymap.set({ "n", "v" }, "<Space>", "<Nop>", { silent = true })

-- For conciseness
local opts = { noremap = true, silent = true }

-- save file
vim.keymap.set("n", "<C-s>", "<cmd> w <CR>", opts)

-- save file without auto-formatting
vim.keymap.set("n", "<leader>sn", "<cmd>noautocmd w <CR>", opts)

-- delete single character without copying into register
vim.keymap.set("n", "x", '"_x', opts)

-- Vertical scroll and center
vim.keymap.set("n", "<C-d>", "<C-d>zz", opts)
vim.keymap.set("n", "<C-u>", "<C-u>zz", opts)
vim.keymap.set("n", "<C-f>", "<C-f>zz", opts)
vim.keymap.set("n", "<C-b>", "<C-b>zz", opts)

-- Find and center
vim.keymap.set("n", "n", "nzzzv", opts)
vim.keymap.set("n", "N", "Nzzzv", opts)

-- Resize with arrows
local function is_real_win(win)
  local bufnr = vim.api.nvim_win_get_buf(win)
  local bt = vim.bo[bufnr].buftype
  local ft = vim.bo[bufnr].filetype
  return bt == "" -- not terminal, quickfix, etc.
    and ft ~= "neo-tree" -- skip Neo-tree
end

-- count how many real windows are visible
local function count_real_wins()
  local cnt = 0
  for _, win in ipairs(vim.api.nvim_list_wins()) do
    if is_real_win(win) then
      cnt = cnt + 1
    end
  end
  return cnt
end

-- only resize when focus is in a real window AND there's >1 real window
local function safe_resize(cmd)
  if
    vim.bo.filetype ~= "neo-tree" -- donâ€™t trigger when Neo-tree is focused
    and count_real_wins() > 1
  then -- need at least 2 real panes
    vim.cmd(cmd)
  end
end

vim.keymap.set("n", "<Up>", function()
  safe_resize("resize -2")
end, opts)
vim.keymap.set("n", "<Down>", function()
  safe_resize("resize +2")
end, opts)
vim.keymap.set("n", "<Left>", function()
  safe_resize("vertical resize -2")
end, opts)
vim.keymap.set("n", "<Right>", function()
  safe_resize("vertical resize +2")
end, opts)
